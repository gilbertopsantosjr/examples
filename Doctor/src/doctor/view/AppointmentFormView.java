    class ComboValue {
        int cod;
        String label;
        ComboValue(int cod, String label){
            this.cod = cod;
            this.label = label;
        }
        @Override
        public String toString() {
            return label;
        }
    }
    
    private void loadAllDoctors(){
        try {
            final DefaultComboBoxModel comboValue = new DefaultComboBoxModel();
            final LoginCtrl loginCtrl = new LoginCtrl();
            List<TeamModel> allDoctors = loginCtrl.getAllMembersByProfile("doctor");
            
            allDoctors.stream().forEach((d) -> { 
                comboValue.addElement(new ComboValue(d.getId(), d.getName()));
            });
            
            doctorToChoose.setModel(comboValue);
                
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, " System failure ! \n " + e.getMessage());
        }
    }

