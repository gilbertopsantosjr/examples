    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat database = new SimpleDateFormat("dd/MM/yyyy HH:mm");
        try {
            ComboValue chosen = (ComboValue) doctorToChoose.getSelectedItem();
            
            
            TeamModel doctor = new TeamModel();
            doctor.setId(chosen.cod);
            
            PatientModel patient = new PatientModel();
            patient.setAddress(address.getText());
            patient.setContactNumber(contactNumber.getText());
            patient.setDateOfBirth(sdf.parse(dateOfBirth.getText()));
            patient.setName(name.getText());
            //setting Id for each in case of update on database
            if(this.idPatient != null){
                patient.setId(this.idPatient);
            }
             
            AppointmentModel appointment = new AppointmentModel();
            appointment.setDateOfAppintment(database.parse(date.getText() + " " +time.getText() ));
            appointment.setDoctor(doctor);
            appointment.setPatient(patient);
            appointment.setNotes("");
            appointment.setPrescription("");
            
            //setting Id for each in case of update on database
            if(idAppointment != null){
                appointment.setId(this.idAppointment);
            }
            
            AppointmentCtrl appointmentCtrl = new AppointmentCtrl();
            appointmentCtrl.save(appointment);
            
            JOptionPane.showMessageDialog(null, " Appointment saved ! \n" );
            // close this window
            dispose();
            
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, " System failure ! \n " + e.getMessage() );
        }
    }//GEN-LAST:event_saveButtonActionPerformed

